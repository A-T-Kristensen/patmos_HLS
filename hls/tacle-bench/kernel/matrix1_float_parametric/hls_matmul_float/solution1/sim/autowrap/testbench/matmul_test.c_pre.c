void matmul_sw(mat_type a[4*4], mat_type b[4*4],
     mat_type sw_result[4*4])
{
  int i, j, k;

  mat_type *p_a = &a[0];
  mat_type *p_b = &b[0];
  mat_type *p_c = &sw_result[0];

  int f;

  for ( k = 0; k < 4; k++ ) {
    p_a = &a[0];

    for ( i = 0; i < 4; i++ ) {

      p_b = &b[k * 4];

      *p_c = 0;
      for ( f = 0; f < 4; f++ )
        *p_c += *p_a++ * *p_b++;

      p_c++;
    }
  }
}

